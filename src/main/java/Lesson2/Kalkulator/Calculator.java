package Lesson2.Kalkulator;

public class Calculator {

    int add(int a, int b) {
        return a + b;
    }

    int substract(int a, int b) {
        return a - b;
    }

    int multiply(int a, int b) {
        return a * b;
    }

    int division(int a, int b) {
        return a / b;
    }
}
